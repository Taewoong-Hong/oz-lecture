// 대입 연산자

let x = 10;

// 복합대입연산자
x += 10; // x = x + 10
console.log(x); // 20
x -= 5; // x = x - 5
console.log(x); // 15

x *= 2; // x = x * 2
console.log(x); // 30
x /= 3; // x = x / 3
console.log(x); // 10

x %= 3; // x = x % 3
console.log(x); // 1
x **= 2; // x = x ** 2
console.log(x); // 1 = 1 * 1

